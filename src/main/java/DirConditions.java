public class DirConditions {
    private int level;
    private Boolean onlyDir;

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public Boolean getOnlyDir() {
        return onlyDir;
    }

    public void setOnlyDir(Boolean onlyDir) {
        this.onlyDir = onlyDir;
    }
}
